/* Regular text */
#define BLK "\x1b[30m"
#define RED "\x1b[31m"
#define GRN "\x1b[32m"
#define YEL "\x1b[33m"
#define BLU "\x1b[34m"
#define MAG "\x1b[35m"
#define CYN "\x1b[36m"
#define WHT "\x1b[37m"

/* Regular bold text */
#define BBLK "\x1b[1;30m"
#define BRED "\x1b[1;31m"
#define BGRN "\x1b[1;32m"
#define BYEL "\x1b[1;33m"
#define BBLU "\x1b[1;34m"
#define BMAG "\x1b[1;35m"
#define BCYN "\x1b[1;36m"
#define BWHT "\x1b[1;37m"

/* Regular underline text */
#define UBLK "\x1b[30m"
#define URED "\x1b[31m"
#define UGRN "\x1b[32m"
#define UYEL "\x1b[33m"
#define UBLU "\x1b[34m"
#define UMAG "\x1b[35m"
#define UCYN "\x1b[36m"
#define UWHT "\x1b[37m"

/* Regular background */
#define BLKB "\x1b[40m"
#define REDB "\x1b[41m"
#define GRNB "\x1b[42m"
#define YELB "\x1b[43m"
#define BLUB "\x1b[44m"
#define MAGB "\x1b[45m"
#define CYNB "\x1b[46m"
#define WHTB "\x1b[47m"

/* High intensty background */
#define BLKHB "\x1b[100m"
#define REDHB "\x1b[101m"
#define GRNHB "\x1b[102m"
#define YELHB "\x1b[103m"
#define BLUHB "\x1b[104m"
#define MAGHB "\x1b[105m"
#define CYNHB "\x1b[106m"
#define WHTHB "\x1b[107m"

/* High intensty text */
#define HBLK "\x1b[90m"
#define HRED "\x1b[91m"
#define HGRN "\x1b[92m"
#define HYEL "\x1b[93m"
#define HBLU "\x1b[94m"
#define HMAG "\x1b[95m"
#define HCYN "\x1b[96m"
#define HWHT "\x1b[97m"

/* Bold high intensity text */
#define BHBLK "\x1b[90m"
#define BHRED "\x1b[91m"
#define BHGRN "\x1b[92m"
#define BHYEL "\x1b[93m"
#define BHBLU "\x1b[94m"
#define BHMAG "\x1b[95m"
#define BHCYN "\x1b[96m"
#define BHWHT "\x1b[97m"

/* Reset */
#define RESET "\x1b[0m"

#endif /* __COLORS_H__ */
